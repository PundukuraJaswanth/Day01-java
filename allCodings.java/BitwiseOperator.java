public class BitwiseOperator {
    public static void main (String[] args){
        int myInt1 = 10;
        int myInt2 = 5;
        int myAdd = myInt1 + myInt2;
        int myOr = myInt1 | myInt2;
        int myXor = myInt1 ^ myInt2;
        System.out.println(myAdd);
        System.out.println(myOr);
        System.out.println(myXor);
    }
}
